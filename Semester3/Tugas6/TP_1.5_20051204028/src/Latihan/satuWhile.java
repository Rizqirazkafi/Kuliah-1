package Latihan;

public class satuWhile {
    public static void main(String[] args) {
        String[] days = {"Senin","Selasa","Rabu","Kamis","Jumat","Sabtu","Minggu"};
        int i=0;
        while(i<days.length){
            System.out.println(days[i]);
            i++;
        }
    }
    
}
